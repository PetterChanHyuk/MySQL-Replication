-- =============================================
-- 복제(Replication) 시나리오 DDL + 더미데이터
-- Source DB에서 실행 → Replica로 복제됨
-- =============================================

CREATE DATABASE IF NOT EXISTS orders_db;
USE orders_db;

-- -----------------------------------------------
-- 1. users
--    - readOnly 라우팅 검증 시 Replica 조회 대상
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS users (
    id         BIGINT AUTO_INCREMENT PRIMARY KEY,
    email      VARCHAR(255) NOT NULL UNIQUE,
    name       VARCHAR(100) NOT NULL,
    created_at DATETIME     DEFAULT NOW()
) ENGINE=InnoDB;

-- -----------------------------------------------
-- 2. orders
--    - RBR 필요성 부각: NOW() 포함 → 행 단위 복제
--    - 세미싱크: 주문 생성 트랜잭션의 ACK 대기 주인공
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS orders (
    id           BIGINT AUTO_INCREMENT PRIMARY KEY,
    user_id      BIGINT                                        NOT NULL,
    status       ENUM('PENDING', 'PAID', 'CANCELLED') DEFAULT 'PENDING',
    total_amount DECIMAL(10, 2)                                NOT NULL,
    created_at   DATETIME                             DEFAULT NOW(),
    CONSTRAINT fk_orders_user FOREIGN KEY (user_id) REFERENCES users (id)
) ENGINE=InnoDB;

-- -----------------------------------------------
-- 3. order_items
--    - orders와 동일 트랜잭션으로 INSERT
--    - GTID가 이 트랜잭션 전체에 단일 ID 부여 → 복제 추적 단위
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS order_items (
    id           BIGINT AUTO_INCREMENT PRIMARY KEY,
    order_id     BIGINT         NOT NULL,
    product_name VARCHAR(255)   NOT NULL,
    quantity     INT            NOT NULL,
    price        DECIMAL(10, 2) NOT NULL,
    CONSTRAINT fk_items_order FOREIGN KEY (order_id) REFERENCES orders (id)
) ENGINE=InnoDB;

-- -----------------------------------------------
-- 4. replication_log
--    - GTID 값을 직접 기록 → Source/Replica 복제 진행 상황 모니터링
--    - "이 트랜잭션이 Replica에 도달했는가?" 검증용
-- -----------------------------------------------
CREATE TABLE IF NOT EXISTS replication_log (
    id           BIGINT AUTO_INCREMENT PRIMARY KEY,
    gtid         VARCHAR(100),
    event_type   VARCHAR(50),
    target_table VARCHAR(100),
    executed_at  DATETIME DEFAULT NOW()
) ENGINE=InnoDB;


